<?php

// Arithmetic Operators

$a = 12;
$b = 5;

echo $a + $b; // Add $a and $b - outputs 17
echo $a - $b; // Subtract $b from $a - outputs 7
echo $a * $b; // Multiply $a by $b - outputs 60
echo $a / $b; // Divide $a by $b - outputs 2.4
echo $a % $b; // Modulo – Reminder of $a / $b
              // Outputs 2
echo -$a;     // Negate $a - Outputs -12
              // This is a unary operator